package com.melodybeauty.melody_beauty_apps.Model;

public class Keluhan {
    private String id;
    private String name;

    public Keluhan(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
